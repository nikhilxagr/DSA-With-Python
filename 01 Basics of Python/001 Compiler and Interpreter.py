# ============================================================
#              COMPILER vs INTERPRETER (Comparison)
# ============================================================

# +--------------------+-----------------------------+-----------------------------+
# | Feature            | Compiler                    | Interpreter                 |
# +--------------------+-----------------------------+-----------------------------+
# | Translation        | Translates whole program    | Translates line by line     |
# |                    | at once                     |                             |
# +--------------------+-----------------------------+-----------------------------+
# | Execution Speed    | Faster (after compilation)  | Slower (runs line by line)  |
# +--------------------+-----------------------------+-----------------------------+
# | Error Detection    | Shows all errors together   | Stops at first error        |
# +--------------------+-----------------------------+-----------------------------+
# | Output             | Generates executable file   | No separate executable file|
# +--------------------+-----------------------------+-----------------------------+
# | Memory Usage       | More memory required        | Less memory required       |
# +--------------------+-----------------------------+-----------------------------+
# | Debugging          | Difficult                   | Easy                        |
# +--------------------+-----------------------------+-----------------------------+
# | Example Languages  | C, C++, Java                | Python, JavaScript, Ruby   |
# +--------------------+-----------------------------+-----------------------------+

# ============================================================
# End of Comparison
