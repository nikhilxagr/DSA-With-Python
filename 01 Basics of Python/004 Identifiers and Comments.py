# ============================================================
#            IDENTIFIERS AND COMMENTS IN PYTHON
# ============================================================

# +----------------------+------------------------------------+
# | Term                 | Description                        |
# +----------------------+------------------------------------+
# | Identifier           | Name used to identify a variable,  |
# |                      | function, class, or object         |
# +----------------------+------------------------------------+
# | Comment              | Text ignored by Python interpreter |
# |                      | used for explanation               |
# +----------------------+------------------------------------+

# ============================================================
#               RULES FOR IDENTIFIERS
# ============================================================

# 1. Must start with a letter (a-z, A-Z) or underscore (_)
# 2. Cannot start with a number
# 3. Can contain letters, numbers, and underscores
# 4. No spaces allowed
# 5. Cannot be a Python keyword
# 6. Identifiers are case-sensitive

# ============================================================
#               VALID IDENTIFIERS (Examples)
# ============================================================

age = 21
_user_name = "Ethical Hacker"
totalMarks = 450
price_2025 = 99.99

# ============================================================
#               INVALID IDENTIFIERS (DO NOT UNCOMMENT)
# ============================================================

# 2age = 20        # ❌ Starts with a number
# user-name = "A" # ❌ Hyphen not allowed
# for = 10         # ❌ Keyword used as identifier
# total marks = 5 # ❌ Space not allowed

# ============================================================
#               COMMENTS IN PYTHON
# ============================================================

